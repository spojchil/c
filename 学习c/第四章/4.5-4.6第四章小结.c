/*

假设输入字符串是：" -13.45e12# 0"

1. 使用`%d`格式（读取整数）：
   • 会读取"-13"（负号+数字）

   • 遇到小数点时停止

   • 变量将得到整数值-13

   • 剩下的输入缓冲区中还有".45e12# 0"


2. 使用`%f`格式（读取浮点数）：
   • 会读取完整的"-13.45e12"（科学计数法表示）

   • 遇到#符号时停止

   • 变量将得到浮点数值-13.45×10¹²

   • 剩下的输入缓冲区中还有"# 0"


3. 使用`%s`格式（读取字符串）：
   • 会读取"-13.45e12#"

   • 遇到空格时停止

   • 变量将得到字符串"-13.45e12#\0"（自动添加结束符）

   • 剩下的输入缓冲区中还有" 0"


4. 使用`%c`格式（读取单个字符）：
   • 只读取第一个字符（这里是空格）

   • 变量将得到空格字符

   • 剩下的输入缓冲区中还有"-13.45e12# 0"


关键理解点：
• `scanf()`会根据格式说明决定何时停止读取

• 停止读取时遇到的字符会留在输入缓冲区中

• 不同类型的格式说明对同一输入会有完全不同的解释

• 空格在`%c`读取时是有意义的，但在其他格式中通常是分隔符

*/

